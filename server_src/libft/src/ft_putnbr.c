#include "libft.h"

void	ft_putnbr(int nbr)
{
	if (nbr < 0)
	{
		nbr *= - 1;
		ft_putchar('-');
	}
	if (nbr >= 10)
	{
		ft_putnbr(nbr / 10);
		ft_putnbr(nbr % 10);
	}
	else
		ft_putchar(nbr + 48);
}
