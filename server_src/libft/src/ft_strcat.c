#include "libft.h"

char	*ft_strcat(char *s1, char const *s2)
{
	char	*s3;

	if (!s1 || !s2)
		return (NULL);
	s3 = s1 + ft_strlen(s1);
	while (*s2)
	{
		*s3 = *s2;
		s2++;
		s3++;
	}
	*s3 = '\0';
	return (s1);
}
