#include "libft.h"

size_t		ft_nbrlen(int n)
{
	size_t	i;

	if (!n)
		return (1);
	i = (n < 0) ? 1 : 0;
	while (n)
	{
		i++;
		n /= 10;
	}
	return (i);
}
