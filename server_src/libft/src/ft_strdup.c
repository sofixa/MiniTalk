#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(char const *str)
{
	int		i;
	char	*new;

	if (!str)
		return (NULL);
	new = (char *) malloc(sizeof(*str) * (ft_strlen(str) + 1));
	if (!new)
		return (NULL);
	i = 0;
	while (str[i])
	{
		new[i] = str[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
